// app.js

document.addEventListener('DOMContentLoaded', () => {

    // --- Инициализация 3D Tilt Эффекта ---
    // Мы находим все элементы с классом .card
    // и применяем к ним магию vanilla-tilt.js
    
    VanillaTilt.init(document.querySelectorAll(".card"), {
        max: 15,     // Максимальный угол наклона (градусы)
        speed: 400,  // Скорость анимации
        glare: true, // Включить эффект "блика"
        "max-glare": 0.5 // Интенсивность блика (0 до 1)
    });

    // Примечание: для этого кода НЕ нужен IntersectionObserver,
    // так как "неоновый глоу" и глитч-эффект работают постоянно,
    // а 3D-эффект активируется при наведении мыши.
    
    console.log("Кибер-Призма v2.0 инициализирована. 3D-карты активны.");
});